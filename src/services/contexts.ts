import { listObjectIndex, listObjects } from './bucket';

export async function getContexts() {
  const indexes = await listObjectIndex();
  const names = indexes.filter((i) => i.name?.includes('contexts')).map((i) => i.name);
  const contexts = await listObjects(names);
  return contexts;
}
